def fmt_num(val: object) -> str:
    """Format a number to avoid 'weird' float displays.
    
    Returns a string representation of the number, rounded to 2 decimal places
    if it's a float, or as an integer if it has no fractional part.
    """
    try:
        f = float(val)
        if f == int(f):
            return str(int(f))
        return f"{f:.2f}".rstrip('0').rstrip('.')
    except (ValueError, TypeError):
        return str(val)

def tr(key: str, **kwargs) -> str:
    """Translate a key into the current language with optional formatting.
    
    If any values in kwargs are numbers, they are formatted via fmt_num().
    """
    try:
        from core.config import load_config
        config = load_config()
        lang = getattr(config, "language", "en")
    except Exception:
        lang = "en"
    
    lang_dict = _TRANSLATIONS.get(lang, _TRANSLATIONS.get("en", {}))
    text = lang_dict.get(key)
    
    if text is None:
        text = _TRANSLATIONS.get("en", {}).get(key)
        if text is None:
            logger.warning(f"Localization key missing: '{key}' (lang: {lang})")
            return key
    
    if kwargs:
        # Pre-format numbers
        formatted_kwargs = {k: (fmt_num(v) if isinstance(v, (int, float)) else v) for k, v in kwargs.items()}
        try:
            return text.format(**formatted_kwargs)
        except (KeyError, ValueError) as exc:
            logger.error(f"Localization format error for key '{key}': {exc}")
            return text
    return text

